# Range is value between the range

print(list(range(10)))
print(list(range(5, 10)))

# print only odd number between 1 and 20
print("The Odd numbers are", list(range(1, 20, 2)))

# print only Even number between 1 and 20
print("The Even numbers are", list(range(0, 20, 2)))

print("The Even numbers in negative are", list(range(10, 0, -2)))

print("The Even numbers in negative are", list(range(-10, -5, 2)))

print("The Negative numbers ", list(range(-10, -5)))

for i in list(range(10)):
    print(i)

# MAX and MIN Numbers

print(max(10, 30, 60, 77))
print(min(10, 30, 60, 77))

